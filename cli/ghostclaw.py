#!/usr/bin/env python3
"""
Ghostclaw CLI — command-line interface for the architectural analyzer.
"""

import sys
import json
import argparse
import subprocess
import datetime
from pathlib import Path
from typing import Dict, Optional

from dotenv import load_dotenv
from core.analyzer import CodebaseAnalyzer

load_dotenv()


def generate_markdown_report(report: Dict) -> str:
    """Generate Markdown content for the architectural report."""
    vibe_score = report['vibe_score']
    stack = report['stack']
    files = report['files_analyzed']
    total = report['total_lines']
    # Use timezone-aware UTC now, then format as ISO with Z suffix for consistency
    now_utc = datetime.datetime.now(datetime.timezone.utc).replace(tzinfo=None)
    timestamp = report.get('metadata', {}).get('timestamp', now_utc.isoformat() + "Z")

    if vibe_score >= 80:
        emoji = "🟢"
    elif vibe_score >= 60:
        emoji = "🟡"
    elif vibe_score >= 40:
        emoji = "🟠"
    else:
        emoji = "🔴"

    lines = [
        f"# Architecture Report — {timestamp}",
        "",
        f"## {emoji} Vibe Score: {vibe_score}/100",
        f"- **Stack**: {stack}",
        f"- **Files Analyzed**: {files}",
        f"- **Total Lines**: {total}",
        ""
    ]

    issues = report.get('issues', [])
    if issues:
        lines.append("## Issues Detected")
        for issue in issues:
            lines.append(f"- {issue}")
        lines.append("")

    ghosts = report.get('architectural_ghosts', [])
    if ghosts:
        lines.append("## 👻 Architectural Ghosts")
        for ghost in ghosts:
            lines.append(f"- {ghost}")
        lines.append("")

    flags = report.get('red_flags', [])
    if flags:
        lines.append("## 🚨 Red Flags")
        for flag in flags:
            lines.append(f"- {flag}")
        lines.append("")

    lines.append("---")
    lines.append("*Generated by Ghostclaw Architectural Assistant*")

    return "\n".join(lines)


def detect_github_remote(repo_path: str) -> Optional[str]:
    """Detect if the repository has a GitHub remote (origin)."""
    try:
        result = subprocess.run(
            ["git", "remote", "get-url", "origin"],
            cwd=repo_path,
            capture_output=True,
            text=True,
            check=False
        )
        if result.returncode == 0:
            url = result.stdout.strip()
            if "github.com" in url:
                return url
    except Exception:
        pass
    return None


def create_github_pr(repo_path: str, report_file: Path, title: str, body: str):
    """Automate PR creation: branch, commit, push, gh pr create."""
    timestamp = datetime.datetime.now().strftime("%Y%m%dT%H%M%S")
    branch_name = f"ghostclaw/arch-report-{timestamp}"

    try:
        # Create branch
        subprocess.run(["git", "checkout", "-b", branch_name], cwd=repo_path, check=True, capture_output=True, text=True)
        # Add report
        subprocess.run(["git", "add", report_file.name], cwd=repo_path, check=True, capture_output=True, text=True)
        # Commit
        subprocess.run(["git", "commit", "-m", f"Add architecture report: {report_file.name}"], cwd=repo_path, check=True, capture_output=True, text=True)
        # Push
        subprocess.run(["git", "push", "origin", branch_name], cwd=repo_path, check=True, capture_output=True, text=True)
        # Create PR
        pr_cmd = ["gh", "pr", "create", "--title", title, "--body", body]
        result = subprocess.run(pr_cmd, cwd=repo_path, capture_output=True, text=True, check=True)
        print(f"🔗 PR created: {result.stdout.strip()}")
    except subprocess.CalledProcessError as e:
        print(f"❌ Failed to create PR: {e.stderr or e}", file=sys.stderr)
    except Exception as e:
        print(f"❌ Error during PR creation: {str(e)}", file=sys.stderr)


def print_report(report: Dict):
    """Pretty-print the analysis report to terminal."""
    vibe_score = report['vibe_score']
    stack = report['stack']
    files = report['files_analyzed']
    total = report['total_lines']

    # Color/emoji based on vibe
    if vibe_score >= 80:
        emoji = "🟢"
    elif vibe_score >= 60:
        emoji = "🟡"
    elif vibe_score >= 40:
        emoji = "🟠"
    else:
        emoji = "🔴"

    print(f"{emoji} Vibe Score: {vibe_score}/100")
    print(f"   Stack: {stack}")
    print(f"   Files: {files}, Lines: {total}")
    print()

    issues = report.get('issues', [])
    if issues:
        print("Issues detected:")
        for issue in issues:
            print(f"  • {issue}")
        print()

    ghosts = report.get('architectural_ghosts', [])
    if ghosts:
        print("👻 Architectural Ghosts:")
        for ghost in ghosts:
            print(f"   {ghost}")
        print()

    flags = report.get('red_flags', [])
    if flags:
        print("🚨 Red Flags:")
        for flag in flags:
            print(f"   {flag}")
        print()

    print("💡 Tip: Run with '--patch' to generate refactor suggestions (not yet implemented)")


def main():
    parser = argparse.ArgumentParser(description="Ghostclaw CLI — Architectural Analyzer")
    parser.add_argument("repo_path", help="Path to the repository to analyze")
    parser.add_argument("--json", action="store_true", help="Output raw JSON")
    parser.add_argument("--no-write-report", action="store_true", help="Skip writing the .md report file")
    parser.add_argument("--create-pr", action="store_true", help="Automatically create a GitHub PR with the report")
    parser.add_argument("--pr-title", help="Custom PR title")
    parser.add_argument("--pr-body", help="Custom PR body")

    args = parser.parse_args()
    repo_path = args.repo_path

    if not Path(repo_path).is_dir():
        print(f"Error: directory not found: {repo_path}", file=sys.stderr)
        sys.exit(1)

    analyzer = CodebaseAnalyzer()
    report = analyzer.analyze(repo_path)

    # 1. Prepare and write report file if needed (must happen before stdout)
    report_file_path = None
    if not args.no_write_report:
        now = datetime.datetime.now().strftime("%Y-%m-%dT%H-%M-%S")
        filename = f"ARCHITECTURE-REPORT-{now}.md"
        report_file_path = Path(repo_path) / filename
        md_content = generate_markdown_report(report)
        try:
            report_file_path.write_text(md_content, encoding='utf-8')
        except Exception as e:
            print(f"Error writing report file: {e}", file=sys.stderr)
            report_file_path = None

    # 2. Output to stdout
    if args.json:
        print(json.dumps(report, indent=2))
    else:
        print_report(report)

    # 3. Print confirmations and suggestions
    # Use stderr for these if --json is active, to keep stdout pipeable
    info_file = sys.stderr if args.json else sys.stdout

    if report_file_path:
        print(f"📝 Report written to: {report_file_path.absolute()}", file=info_file)

    remote_url = detect_github_remote(repo_path)
    if remote_url and not args.create_pr:
        print(f"💡 Tip: This repository has a GitHub remote.", file=info_file)
        print(f"   To create a PR with this report, run:", file=info_file)
        print(f"   ghostclaw \"{repo_path}\" --create-pr", file=info_file)

    # 4. Create PR if requested
    if args.create_pr:
        if not report_file_path:
            print("Error: Cannot create PR because report file was not written.", file=sys.stderr)
            return

        title = args.pr_title or f"🏰 Architecture Report - {datetime.datetime.now().strftime('%Y-%m-%d')}"
        body = args.pr_body or f"Ghostclaw has completed an architectural review of the codebase.\n\n**Vibe Score: {report['vibe_score']}/100**\n\nPlease review the attached report for details."
        create_github_pr(repo_path, report_file_path, title, body)


if __name__ == "__main__":
    main()
